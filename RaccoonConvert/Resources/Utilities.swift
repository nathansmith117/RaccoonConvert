import Foundation

// Decimal, hex, binary, oct.

// Decimal.
func toDecimal(value: Int) -> String?
{
    let converted: String? = String(value)
    return converted
}

func fromDecimal(value: String) -> Int?
{
    let converted: Int? = Int(value)
    return converted
}


